    public DefaultTableModel initTableEditeur() {
        Vector vData = new Vector();
        Vector vNoms = new Vector();
        vNoms.addElement("Editeur");
        try {
            ResultSet rs = gestion.sqlRead();
            while (rs.next()) {
                Vector vLigne = new Vector();
                vLigne.addElement(new Editeur(rs.getString(1)));
                vData.add(vLigne);
            }
        } catch (SQLException ex) {
            System.out.println(ex);
            if (ex.getErrorCode() == 2627) {
                JOptionPane.showMessageDialog(this, "Editeur déjà présent dans la Base de Données", "Erreur", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        return new DefaultTableModel(vData, vNoms);
    }

