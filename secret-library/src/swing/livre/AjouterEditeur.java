    private void boutonAjouterEditeurActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonAjouterEditeurActionPerformed
        // TODO add your handling code here:
        if (!champAjouterEditeur.getText().trim().isEmpty()) {
            try {
                editeur = new Editeur(champAjouterEditeur.getText());
                gestion.sqlCreate(editeur);
                tableEditeur.setModel(initTableEditeur());
            } catch (SQLException ex) {
                System.out.println(ex);
                if (ex.getErrorCode() == 2627) {
                    JOptionPane.showMessageDialog(this, "Editeur déjà présent dans la Base de Données", "Erreur", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_boutonAjouterEditeurActionPerformed

    private void boutonValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonValiderActionPerformed
        // TODO add your handling code here:
        if (tableEditeur.getSelectedRow() > -1) {
            editeur = (Editeur) tableEditeur.getValueAt(tableEditeur.getSelectedRow(), 0);
            dispose();
        }
    }//GEN-LAST:event_boutonValiderActionPerformed

    private void boutouRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutouRechercherActionPerformed
        // Row filter qui filtre le table model selon une regex
        // regex : (?iu) permet d'ignorer la case et les accents
        RowFilter<DefaultTableModel, Editeur> rf = null;
        try {
            rf = RowFilter.regexFilter("(?iu)" + champRecherche.getText());
        } catch (java.util.regex.PatternSyntaxException e) {
            System.out.println("erreur saisie");
            return;
        }
        sorter.setRowFilter(rf);
    }//GEN-LAST:event_boutouRechercherActionPerformed

